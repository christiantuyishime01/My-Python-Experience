#!/usr/bin/env python3
grades = [78, 58, 23, 32, 53, 44, 43, 21]
# 1. Access values present in the List/ArrayList and print the value at index 2
value_at_index_2 = grades[2]
print(f"The value at index 2 is: {value_at_index_2}")
# 2. Perform an addition operation of the value at index 2 and the value at index -2
value_at_index_minus_2 = grades[-2]
sum_of_values = value_at_index_2 + value_at_index_minus_2
print(f"The sum of the value at index 2 and the value at index -2 is: {sum_of_values}")
